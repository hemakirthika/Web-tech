  <h1>Capital-Gandhinagar</h1>
  <h2>History</h2>
  <img src="https://upload.wikimedia.org/wikipedia/commons/7/77/Akshardham_Gandhinagar_Gujarat.jpg" class="thumbnail" alt="Dhuska" width="304" height="236">
  <p>Gandhinagar, Gujarat is the capital of the state of Gujarat in India. Gandhinagar is located approximately 23 km north of Ahmedabad, on the west central point of the industrial corridor between the metropolitan cities of Delhi and Mumbai.
  Gandhinagar lies on the west bank of the Sabarmati River, about 545 km (338 miles) north of Mumbai and 901 km (560 miles) southwest of Delhi.The Akshardham temple is located in Gandhinagar.There was a determination to make Gandhinagar a purely Indian enterprise, partly because the state of Gujarat was the birthplace of Mahatma Gandhi. For this reason, the planning was done by two Indian town planners: Prakash M Apte and H. K. Mewada, who had apprenticed with Le Corbusier in Chandigarh.</p>
  <h1>Emblem</h1>
  <img src="https://www.techniajz.com/admin/ckeditor/ckfinder/userfiles/images/blog_images/Gujarat-state-emblem-750x470.jpg?quality=90" class="thumbnail" alt="Dhuska" width="304" height="236">
  <p>It is an adaptation of the Lion Capital of Ashoka, a statue that served as an emblem on the Ashoka Pillars constructed by Emperor Ashoka of the Mauryan Empire between 269 and 232 BCE.
    It is a dimensional sculpture in the shape of four lions, each facing a different direction.
    At Sarnath, the actual capital, four Asiatic lions are arranged back-to-back, symbolizing power, courage, confidence, and pride, and the entire structure is mounted on a circular base. The bottom of the image has a horse and a bull, while the center contains a Dharma chakra.
    Over an abacus with a full blossom of lotus illuminating the fountainhead of life and creative inspiration, there is a frieze of sculptures depicting the Lion of the North, The Horse of the West, The Bull of the South, and the Elephant of the East.
    This polished stone capital is formed from a single block of sandstone, crowned by a wheel of the law.
    Below the abacus in Devanagari script is the motto: Satyameva Jayate, which is an integral part of the emblem. Quoted from the Mundaka Upanishad, a sacred Hindu text.
    </p>
