      <h1>Famous Food</h1>
  <h2>1.Khaman - The Sponge Snack</h2>
  <img src="https://www.holidify.com/images/cmsuploads/compressed/Dhokla_20180327130008.jpg" class="thumbnail" alt="Dhuska" width="304" height="236">
  <p>A tasty and healthy steamed snack made from freshly ground lentils and chickpea flour, it is very similar to its humble cousin, the world-famous -dhokla. To prepare the khaman, the khaman flour mix is boiled along with turmeric, salt and baking soda to make it light and fluffy. It is then cut up into cubes and usually garnished with mustard seeds, coriander leaves, sev and chopped onions. Traditionally served on a large green leaf called the Kesuda, the modern, urban version is served in newspapers in farsan (snack) shops with tangy chutneys and several pieces of green chillies. Popular adaptations of the khaman include Ameri khaman (mashed up khaman garnished with sev and pomegranate), Nylon khaman (softer and fast-cooking khaman) and Masala khaman (khaman served with hot and spicy chilly powder)</p> 
  <h2>2. Thepla - A Snack that every Gujarati Swear by!</h2>
  <img src="https://www.holidify.com/images/cmsuploads/compressed/Tehepla_20180327130226.JPG" class="thumbnail" alt="Dhuska" width="304" height="236">
  <p>No Gujarati journeys, picnics, foreign trips or even business trips are complete without this ubiquitous snack. Made from gram flour, whole wheat flour, fresh fenugreek leaves and spices, these flatbreads are healthy snacking options with a long shelf life. Usually served piping hot with fresh curd, pickles or chundo, they make wholesome meals. When accompanied by a cup of steaming hot tea, they also make a sumptuous breakfast or a great snack during the monsoon. Although the methi (fenugreek) ones are the most common, other varieties include palak (spinach), amaranth or muli (raddish) theplas-specially recommended for fussy eaters.</p>

