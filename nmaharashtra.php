  <h2>2.Vada Pav</h2>
  <img src="mf2.jpg" class="thumbnail" alt="Dhuska" width="304" height="236">
  <p>Vada Pav can easily be called the Indian version of a burger! A delicious, spicy, deep-fried patty made of potato is squeezed between a thick slice of bread, similar to a burger bun. Between the vada (patty) and pav (bread) a spicy mix of spices and salt is sprinkled and garnished with a fried, salted green chilli. Sounds perfect, isn't it? It's an all-day snack and is extremely popular among Maharashtrians. It is very economical, filling and easily accessible. Another popular offshoot is the Bhajiya Pav, which has batter-fried onions instead of the patty.</p>

