  <h2>History</h2>
  <img src="mc.jpg" class="thumbnail" alt="Dhuska" width="304" height="236">
  <p>Indigenous tribals have inhabited Mumbai (Bombay) since the Stone Age. The Kolis and Aagri (a Marathi-Konkani people)were the earliest known settlers of the islands. Between the 2nd century BCE and 10th century CE, the islands came under the control of successive indigenous dynasties: the Satavahanas, Abhiras, Vakatakas, Kalachuris, Konkan Mauryas, Chalukyas, Rashtrakutas, Silharas & Chollas.
    Bhima of Mahikavati established a small kingdom in the area during the late 13th century, and brought settlers. The Delhi Sultanate captured the islands in 1348, and they were later passed to the Sultanate of Guzerat from 1391. The Treaty of Bassein (1534) between the Portuguese viceroy Nuno da Cunha and Bahadur Shah of Gujarat, placed the islands into Portuguese possession in 1534.
    The islands suffered the Maratha Invasion of Goa and Bombay, and the Mughal invasions of Konkan (1685) towards the end of 17th century.[citation needed] During the English East India Company's rule in mid-18th century, it emerged as an important port city, having maritime trade contacts with Mecca, Basra etc.[citation needed] Economic development characterised British Bombay in the 19th century, the first-ever Indian railway line commenced operations between Bombay harbour and Taana city in 1853. Since the early 1900s, the city has also the home base of the Bollywood film industry. The city became a strong base for the Indian independence movement during the early 20th century, it was the centre of the Rowlatt Satyagraha of 1919 and Royal Indian Navy Mutiny of 1946.After India's independence in 1947, the territory of Bombay Presidency retained by India was restructured into Bombay State. The area of Bombay State increased, after several erstwhile princely states that joined the Indian union were integrated into Bombay State.
    In 1960, following protests from the Samyukta Maharashtra movement, the city was incorporated into the newly created Maharashtra state from Bombay state. The Bombay metro area faced some unfortunate events like the inter-communal riots of 1992–93, while the 1993 Mumbai bombings caused extensive loss of life and property. Bombay was renamed Mumbai on 6 March 1996.</p>
