      <h1>Famous Food</h1>
  <h2>1.Uttappam
  </h2>
  <img src="https://www.holidify.com/blog/wp-content/uploads/2015/11/Mini_Uttappam.jpg" class="thumbnail" alt="Dhuska" width="304" height="236">
  <p>Uttapam is a dish very similar to the dosa and is a speciality of Tamil Nadu food. It is made by grinding the Urad Dal and rice powder together. It is then allowed to ferment for a few hours and then slow fried over very minimal oil over a skillet. Uttapam is flat and crispy at the sides just like dosa but soft and doughy in the middle like an Idli. The uttapam is then given a little topping of tomatoes, onions, capsicum etc. It tastes delicious with either coconut chutney or Sambar, however, I would recommend the coconut chutney. You can also try it with the spicy tomato and chilli chutney which tastes quite heavenly.</p> 
  <h2>2.Rasam
  </h2>
  <img src="https://www.holidify.com/blog/wp-content/uploads/2015/11/GarlicRasam.jpg" class="thumbnail" alt="Dhuska" width="304" height="236">
  <p>Rasam is my personal favourite! Tamarind forms the base of this soup-like dish, and added to it are tomatoes, pepper and cumin seed. The rasam is almost a pre-meal appetizer, which is light and doesn't fill you up too much before a meal. The rasam is a famous dish that is poured over the rice like a normal curry. Though it is almost colourless, it has a burst of flavour. You can also drink it as you would a soup. It is good for a sore throat too.</p>
