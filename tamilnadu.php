  <h1>Capital-Chennai</h1>
  <h2>History</h2>
  <img src="https://upload.wikimedia.org/wikipedia/commons/thumb/1/12/Madras_City_1909.jpg/330px-Madras_City_1909.jpg" class="thumbnail" alt="Tamil Nadu" width="304" height="236">
  <p>Chennai, formerly known as Madras, is the capital of the state of Tamil Nadu and is India's fifth largest city.[1] It is located on the Coromandel Coast of the Bay of Bengal. With an estimated population of 8.9 million (2014), the 383-year-old city is the 31st largest metropolitan area in the world.
    Chennai boasts a long history from the English East India Company, through the British rule to its evolution in the late 20th century as a services and manufacturing hub for India. Additionally, the pre-city area of Chennai has a long history within the records of South Indian Empires.</p>
  <h1>Emblem</h1>
  <img src="https://upload.wikimedia.org/wikipedia/commons/thumb/8/81/TamilNadu_Logo.svg/180px-TamilNadu_Logo.svg.png" class="thumbnail" alt="Dhuska" width="304" height="236">
  <p>The state emblem was designed in 1949 by artist R. Krishna Rao who was a native of Madurai.Krishna Rao was honoured with awards and titles for his contribution to the state.A student of the Government College of Fine Arts and Crafts in Chennai, who went on to become the principal of the college later, Rao was approached to design the emblem in 1948 when he was a professor of applied arts in the college.
   It consists of the Lion Capital of Ashoka without the bell lotus foundation and flanked on either side by an Indian flag. Behind the capital, is the image of a Gopuram or Hindu temple tower based on the Srivilliputhur Andal temple . Around the rim of the seal runs an inscription in Tamil script, one at the top தமிழ் நாடு அரசு ("Tamil Nadu Arasu" which translates to "Government of Tamil Nadu") and the other at the bottom வாய்மையே வெல்லும் ("Vaymaiye Vellum" which translates to "Truth Alone Triumphs" also commonly known as "Satyameva Jayate" in Sanskrit). It is the only state emblem that has the Indian Flag and Hindu temple tower on its seal.</p>
