  <h1>Capital-Ranchi</h1>
  <h2>History</h2>
  <img src="jc.jpg" class="thumbnail" alt="Dhuska" width="304" height="236">
  <p>Ranchi is the capital of the Indian state of Jharkhand.Ranchi was the centre of the Jharkhand movement,which called for a separate state for the tribal regions of South Bihar, northern Odisha, western West Bengal and the eastern area of what is present-day Chhattisgarh. The Jharkhand state was formed on 15 November 2000 by carving out the Bihar divisions of Chota Nagpur and Santhal Parganas. Ranchi has been selected as one of the hundred Indian cities to be developed as a smart city under PM Narendra Modi's flagship Smart Cities Mission.
   Ranchi is also one of the oldest cities in Jharkhand. Jagannath Temple and Ratu Palace are some sights which witnessed the history of Ranchi.
   Ranchi is rapidly growing its economy, and certain parks, special economic zones and industrial areas are being developed. Of late, new sectors and modern areas have been built for the city's development.Ranchi is also nicknamed the City of Waterfalls.</p>
  <h1>Emblem</h1>
  <img src="je.jpg" class="thumbnail" alt="Dhuska" width="304" height="236">
  <p>The first emblem of Jharkhand was adopted on 15 November 2000 when Jharkhand state was formed from the southern part of Bihar. This emblem consisted of an Ashoka Chakra, as depicted on the national Flag of India, surrounded by four letters Js stylised as daggers. The legend underneath, Jharkhand Sarkar, translates as Government of Jharkhand.
    In January 2020, the new Chief Minister of Jharkhand, Hemant Soren announced that a new state emblem is to be adopted soon.He stated that the new emblem should represent the culture, tradition, history ,and future of the state and invited entries for a new design via a public competition.A purported winning design, depicting a tree, was subsequently reported by several media outlets but this was later revealed to be a hoax.
    On 22 July a new emblem was officially approved to be used from 15 August 2020</p>
