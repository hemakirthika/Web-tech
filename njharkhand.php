      <h1>Famous Food</h1>
  <h2>1.Dhuska</h2>
  <img src="jf1.jpg" class="thumbnail" alt="Dhuska" width="304" height="236">
  <p>Dhuska is one of the most popular foods in Jharkhand, and it can be found in almost every home. Without trying Dhuska, your trip to Jharkhand is incomplete. It is often eaten for breakfast and is made with a rice and lentil batter that is then fried. It’s usually served with traditional ghugni, which is a basic black chickpea stew. Dhuska and ghugni are a delicacy that should be on your list of must-try foods!</p> 
  <h2>2.Chilka Roti</h2>
  <img src="jf2.jpg" class="thumbnail" alt="Dhuska" width="304" height="236">
  <p>Jharkhand is a state in eastern India that was a part of Bihar until 2000, and rice is a staple meal here, as it is in Bihar. Rice and besan flour are part of most of Jharkhand’s traditional food. Chilka Roti is made using Rice flour, chana dal, and urad dal, It is quite similar to Dosa prepared in South India, yet has a distinct flavour. This meal is served with mutton curry or Oal ki sabzi. Chilka roti is a popular supper meal in Jharkhand.</p>

